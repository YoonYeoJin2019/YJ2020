package poly.dto;

public class ExplainDTO {

	private String explain_no;
	private String explain_content;
	private String explain_img;
	private String health_no;
	
	
	
	public String getExplain_no() {
		return explain_no;
	}
	public void setExplain_no(String explain_no) {
		this.explain_no = explain_no;
	}
	public String getExplain_content() {
		return explain_content;
	}
	public void setExplain_content(String explain_content) {
		this.explain_content = explain_content;
	}
	public String getExplain_img() {
		return explain_img;
	}
	public void setExplain_img(String explain_img) {
		this.explain_img = explain_img;
	}
	public String getHealth_no() {
		return health_no;
	}
	public void setHealth_no(String health_no) {
		this.health_no = health_no;
	}
	
	
	
	
	
}
