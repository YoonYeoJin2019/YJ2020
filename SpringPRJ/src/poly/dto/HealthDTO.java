package poly.dto;

public class HealthDTO {

	private String health_no;
	private String health_name;
	private String health_file;
	private String health_file2;
	private String health_img;
	
	
	public String getHealth_no() {
		return health_no;
	}
	public void setHealth_no(String health_no) {
		this.health_no = health_no;
	}
	public String getHealth_name() {
		return health_name;
	}
	public void setHealth_name(String health_name) {
		this.health_name = health_name;
	}
	public String getHealth_file() {
		return health_file;
	}
	public void setHealth_file(String health_file) {
		this.health_file = health_file;
	}
	public String getHealth_file2() {
		return health_file2;
	}
	public void setHealth_file2(String health_file2) {
		this.health_file2 = health_file2;
	}
	public String getHealth_img() {
		return health_img;
	}
	public void setHealth_img(String health_img) {
		this.health_img = health_img;
	}
	
	
}
